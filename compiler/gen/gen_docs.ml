(** Bakes manual and example source files into compiler/lib/embedded_docs.ml.

    Invoked automatically by `dune build` via the rule in compiler/lib/dune.
    Can also be run standalone for debugging:
      dune exec gen/gen_docs.exe -- <repo-root>

    Key format (relative to repo root):
      "manual/<file>.md"               manual section files
      "LANGUAGE-SPEC.md"               root-level docs
      "example/<file>.tesl"            top-level examples
      "example/learn/<lesson>.tesl"    lesson files
      "example/kanel/<module>.tesl"    kanel multi-module example
*)

let emit oc key path =
  match (try Some (In_channel.with_open_text path In_channel.input_all)
         with Sys_error _ -> None) with
  | None -> ()
  | Some content ->
    Printf.fprintf oc "  (%S,\n   %S);\n\n" key content

(* The LSP writes transient validation copies named `tesl-lsp-<n>.tesl` into the
   directory of the file being edited; they must never be baked into the embedded
   docs (else embedded_docs.ml churns on every build). *)
let is_transient f =
  String.length f >= 9 && String.sub f 0 9 = "tesl-lsp-"

let walk_dir dir key_prefix ext oc =
  if Sys.file_exists dir then begin
    let files = Sys.readdir dir |> Array.to_list |> List.sort String.compare in
    List.iter (fun f ->
      if (not (is_transient f)) && List.exists (Filename.check_suffix f) ext then
        emit oc (key_prefix ^ f) (Filename.concat dir f)
    ) files
  end

let () =
  (* Find the repo root by walking up from the executable location until we find
     a directory that contains both "manual/" and "example/".  This is robust to
     whether the generator is invoked from dune (inside _build/) or directly. *)
  let find_repo_root start =
    let rec up dir count =
      if count > 8 then dir  (* safety limit *)
      else if Sys.file_exists (Filename.concat dir "manual")
           && Sys.file_exists (Filename.concat dir "example")
      then dir
      else up (Filename.dirname dir) (count + 1)
    in
    (* Resolve any symlinks so dirname works correctly *)
    let resolved = try Unix.realpath start with _ -> start in
    up (Filename.dirname resolved) 0
  in
  let repo_root =
    if Array.length Sys.argv > 1
       && Sys.file_exists (Filename.concat Sys.argv.(1) "manual") then
      Sys.argv.(1)
    else
      find_repo_root Sys.argv.(0)
  in

  let oc = stdout in

  Printf.fprintf oc
    "(** Embedded documentation and examples — auto-generated by gen/gen_docs.ml.\n\
     \n\
     Regenerated automatically on every `dune build`; do not edit by hand.\n\
     The generator reads from manual/ and example/ in the repo root.\n\
     *)\n\n";

  Printf.fprintf oc "let files : (string * string) list = [\n\n";

  (* Root-level docs *)
  List.iter (fun f ->
    emit oc f (Filename.concat repo_root f)
  ) ["LANGUAGE-SPEC.md"; "TESL.md"; "README.md"; "INSTALL.md"];

  (* manual/ *)
  let manual_dir = Filename.concat repo_root "manual" in
  walk_dir manual_dir "manual/" [".md"] oc;

  (* dev-docs/ — the contributor docs live at the REPO ROOT (dev-docs/), not
     under manual/.  Earlier this walked manual/dev-docs/ which does not exist,
     so `tesl help manual dev` was broken in a packaged binary (the dev docs
     were never embedded).  Embed them under the key that matches their real
     on-disk path so the CLI's disk and embedded lookups agree. *)
  let dev_dir = Filename.concat repo_root "dev-docs" in
  walk_dir dev_dir "dev-docs/" [".md"] oc;

  (* example/*.tesl (top-level, single-file examples) *)
  let example_dir = Filename.concat repo_root "example" in
  walk_dir example_dir "example/" [".tesl"] oc;

  (* example/learn/ — lesson files and lesson prose docs *)
  let learn_dir = Filename.concat example_dir "learn" in
  walk_dir learn_dir "example/learn/" [".tesl"; ".md"] oc;

  (* example/intro/ — the prose "TLDR of the whole language" newcomer track (D17).
     Previously NOT embedded, so `tesl help` in a packaged binary could not serve
     the intro slides even though examples.md links them. *)
  let intro_dir = Filename.concat example_dir "intro" in
  walk_dir intro_dir "example/intro/" [".md"] oc;

  (* example/kanel/ — multi-module SaaS example *)
  let kanel_dir = Filename.concat example_dir "kanel" in
  walk_dir kanel_dir "example/kanel/" [".tesl"] oc;

  (* example/chat/ *)
  let chat_dir = Filename.concat example_dir "chat" in
  walk_dir chat_dir "example/chat/" [".tesl"] oc;

  (* example/medical-journal_wip/ *)
  let med_dir = Filename.concat example_dir "medical-journal_wip" in
  walk_dir med_dir "example/medical-journal_wip/" [".tesl"] oc;

  Printf.fprintf oc "]\n\n";
  Printf.fprintf oc "let lookup (key : string) : string option =\n";
  Printf.fprintf oc "  List.assoc_opt key files\n\n";
  Printf.fprintf oc
    "(** Lookup by basename only — useful when the exact subpath is unknown. *)\n";
  Printf.fprintf oc "let lookup_by_basename (name : string) : string option =\n";
  Printf.fprintf oc
    "  match List.find_opt (fun (k, _) -> Filename.basename k = name) files with\n";
  Printf.fprintf oc "  | Some (_, v) -> Some v\n";
  Printf.fprintf oc "  | None -> None\n\n";
  Printf.fprintf oc "(** All embedded keys. *)\n";
  Printf.fprintf oc "let all_keys () : string list = List.map fst files\n"
